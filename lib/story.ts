export const REGION_NAMES: string[] = REGIONS.map((r) => r.name);

export function regionName(stage: number): string {
  return REGIONS[(stage - 1) % REGIONS.length].name;
}

export function characterFor(stage: number): RegionCharacter {
  return REGIONS[(stage - 1) % REGIONS.length].character;
}

// 지역 통과 시 얻는 '물음'(예전 clue 역할)
export function clueFor(stage: number): string {
  return characterFor(stage).question;
}

